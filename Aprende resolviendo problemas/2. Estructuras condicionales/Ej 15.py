# Escribe un programa que acepte un número del 1 al 7 y muestre el nombre del 
# día. 

dia = int(input("INtroduce un número entre el 1-7: "))

if dia == 1:
    print("Lunes")
elif dia == 2:
    print("Martes")
elif dia == 3:
    print("Miercoles")
elif dia == 4:
    print("Jueves")
elif dia == 5:
    print("Viernes")
elif dia == 6:
    print("Sábado")
elif dia == 7:
    print("Domingo")
else: 
    print()